func (suite *MidiMessageTestSuite) TestConvertTempo() {
	converted, err := messages.Convert(suite.tempo)
	suite.NoError(err)
	suite.Equal(types.MidiTempo(), converted.Op)
	suite.Equal(uint8(68), converted.Args.Tempo)
}

